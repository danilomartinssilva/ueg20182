        public void adicionarDadosJson(){
        JSONObject jsObj =new JSONObject();
        FileWriter file = null;      
        jsObj.put("nome", clientes.getNome());
        jsObj.put("email", clientes.getEmail());
        jsObj.put("idade", clientes.getIdade());
        jsObj.put("cidade", clientes.getCidade());
        jsObj.put("endereco", clientes.getEndereco());
        jsObj.put("estado", clientes.getEstado());         
        
        
        
        try{
            file = new FileWriter("saida.json");
            file.write(jsObj.toJSONString());
            file.flush();            
            file.close();
            JOptionPane
              .showMessageDialog(rootPane, "Adicionado com sucesso!");
        }
         catch (FileNotFoundException  e) {
            e.printStackTrace();
        }  
       
        catch (IOException e ){
            e.getMessage();
        }
    }
     public void lerDadosJson(){
             JSONParser parser = new JSONParser();
 
        try {
 
            Object obj = parser.parse(new FileReader(
                    "saida.json"));
 
            JSONObject jsonObject = (JSONObject) obj; 
            String name = (String) jsonObject.get("nome");         
            System.out.println("Name: " + name);                     
        } catch (FileNotFoundException  e) {
            e.printStackTrace();
        }  
        catch(ParseException e){
            e.printStackTrace();
        }
        catch (IOException e ){
            e.getMessage();
        }
        catch(NullPointerException e){
            e.getMessage();
        }
        catch(ClassCastException e){
            e.getMessage();
        }
     }
